function displayMeny() {
 document.getElementById ("menu-small").style.display = "none";
 document.getElementById ("close-menu").style.display = "inline-block";
 document.getElementById ("nav-bar").style.display = "inline";

}
function closeMenu (){
    document.getElementById ("close-menu").style.display = "none";
    document.getElementById ("nav-bar").style.display = "none";
    document.getElementById ("menu-small").style.display = "block";
   
}


